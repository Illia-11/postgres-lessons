/*
  Команди SQL поділяється на певні "категорії" які називаються підмовами (sublanguages)

  DDL (Data Defenition Language) - мова визначення даних
    використовується для опису структури даних
      команди розпочинаються з ключових слів CREATE, DROP, ALTER, TRUNCATE

  DML (Data Manipulation Language) - мова маніпуляцій даних
    використовується для зміниї даних БД
      команди розпочинаються з ключових слів INSERT, UPDATE, DELETE

  DQL (Data Query Language) - мова запитів даних
    використовується для отримання даних з БД
      SELECT

  DCL (Data Control Language) - мова контролю даних
    повʼязане з адмініструванням

  TCL (Transaction Control Language) - мова контролю транзакцій
*/